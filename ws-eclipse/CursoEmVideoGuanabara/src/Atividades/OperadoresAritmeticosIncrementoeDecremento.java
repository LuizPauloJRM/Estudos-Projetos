package Atividades;

public class OperadoresAritmeticosIncrementoeDecremento {

	public static void main(String[] args) {
		int x=20;
		int y=30;
		float m=(x + y)/2;
		System.out.println("A média de x e y é igual a: " +m);
		int numero = 10;
		numero ++;//soma mais 1 a atribuição 10
		System.out.println(numero);
		numero --;
		System.out.println(numero);
		int valor = 7 + numero++;//Utilize o numero depois de fazer a soma , soma mais 1 no número
		System.out.println(valor);
	}

}
