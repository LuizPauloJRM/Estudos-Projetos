package ForWhile;
import java.util.Scanner;
public class Ex05While01 {

	public static void main(String[] args) {
	Scanner Scanner= new Scanner(System.in);
	int i;
	for(i=3;i<=10;i=2) {
		System.out.println("______________");	
	}

	i=0;
	while(i<=10) {
		System.out.println(i+")Luiz Paulo");
	}
	}

}
