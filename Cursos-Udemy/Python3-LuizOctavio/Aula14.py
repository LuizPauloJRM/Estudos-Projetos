#Blocos de código + if/elif(condicionais)
#Introdução aos blocos de códigos+if/elif/else(condicionais)
#se/se nao se/se nao
entrada=input('Digite "entrar" ou "sair"')
if entrada=='entrar':
    print('Voce entrou no sistema')
else:
    print('Voce saiu do sistema')
#elif
#Se voce nao digitar nenhuma dessas condições “sair” ou “entrar”
entrada=input('Digite "entrar" ou "sair"')
if entrada =='entrar':
    print('Voce entrou no sistema')
elif entrada =='sair':
    print('Voce saiu do sistema')
else:
    print('Digite entrar ou sair')